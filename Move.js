function Moveleft(){
    document.getElementById("rectangle").style.left = "0px";
    document.getElementById("rectangle").style.backgroundColor = "blue";

}

function Moveright(){
    document.getElementById("rectangle").style.left="880px";
    document.getElementById("rectangle").style.backgroundColor = "yellow";
}
function Moveup(){
    document.getElementById("rectangle").style.bottom="400px";
    document.getElementById("rectangle").style.backgroundColor = "blue";

}
function Movedown(){
    document.getElementById("rectangle").style.bottom="0px";
    document.getElementById("rectangle").style.backgroundColor = "red";

}

